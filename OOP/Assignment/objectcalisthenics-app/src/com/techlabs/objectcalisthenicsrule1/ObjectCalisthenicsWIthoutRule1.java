package com.techlabs.objectcalisthenicsrule1;

public class ObjectCalisthenicsWIthoutRule1 {
	public static void main(String[] args) {

		String name = "Swabhav";
		int number = 10;
		if (name.equals(name)) {
			System.out.println(name);
			
			if (number == 10) {
				System.out.println(number);

				if (name.equals("Techlabs")) {
					System.out.println(name);
				}
			}

		}
	}
}
